function button1(){
    var b1  = document.getElementById('li-button1').style;
    var b2  = document.getElementById('li-button2').style;
    var b3  = document.getElementById('li-button3').style;

    b1.backgroundColor="#0674fd";
    b2.backgroundColor="rgb(238, 249, 255)";
    b3.backgroundColor="rgb(238, 249, 255)";
    b1.color = "white";
    b2.color = "#222";
    b3.color = "#222";

    //for content
    var div1 = document.getElementById('content-div1').style;
    var div2 = document.getElementById('content-div2').style;
    var div3 = document.getElementById('content-div3').style;
    

    div1.display = "block";
    div2.display = "none";
    div3.display = "none";




}
